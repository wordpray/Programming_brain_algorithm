//1-100までの番号で書かれた100枚のカードを順番に並べられています。
//最初、全てのカードは裏返しの状態である人が、2番のカードから1枚おきにカードを裏返していきます。
//2‥4‥6‥100番のカードを表が向くようになります。次に別の人が、3番のカードから2枚おきにカードを裏返していきます。
//また別の人が、4番のカードから3枚おきにカードを裏返していきます。
//このようにN番目のカードからｎ-1枚おきにカードを裏返す操作を、どのカードの向きが変わらなくなるまで続ける。

N = 100
cards = Array.new(N, false)

(2..100).each do |i|
  j = i -1
  while (j < cards.size)
    cards[j] = !cards[j]
    j += i
  end
end

N.times do |i|
 puts i + 1 if !cards[i]
end
